package operator;

public class test1 {

	public static void main(String[] args) {
		int n1 = 200 ; 
		int n2 = 100 ; 
		
		System.out.println(n1 == n2);
		System.out.println(n1 == n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		System.out.println(n1 != n2);
		System.out.println(n1 != n2);

		
		System.out.println("더하기 : " + (n1 + n2));
		System.out.println("빼기 : " + (n1 - n2));
		System.out.println("곱하기 : " + n1 * n2);
		System.out.println("나누기 : " + n1 / n2);
		
	}

}
